        public string this[string key] => key != null && _t.ContainsKey(_currentLanguage) && _t[_currentLanguage].ContainsKey(key) ? _t[_currentLanguage][key] : key ?? string.Empty;
        public string this[string lang, string key] => key != null && _t.ContainsKey(lang) && _t[lang].ContainsKey(key) ? _t[lang][key] : key ?? string.Empty;
        
        public void AddOrUpdateTranslation(string lang, string key, string value)
        {
            if (key != null && value != null && _t.ContainsKey(lang))
            {
                _t[lang][key] = value;
            }
        }

